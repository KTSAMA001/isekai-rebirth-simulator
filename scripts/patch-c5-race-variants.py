# 加载、打补丁、写回
patched_count = 0
for fn in os.listdir(EVENT_DIR):
    if not fn.endswith('.json'):
        continue
    fp = os.path.join(EVENT_DIR, fn)
    with open(fp, 'r', encoding='utf-8') as f:
        events = json.load(f)
    
    changed = False
    for event in events:
        if event['id'] in RACE_VARIANTS:
            event['raceVariants'] = RACE_VARIANTS[event['id']]
            patched_count += 1
            changed = True
            print(f"  [+] {fn}: {event['id']} <- {list(event['raceVariants'].keys())}")
    
    if changed:
        with open(fp, 'w', encoding='utf-8') as f:
            json.dump(events, f, ensure_ascii=False, indent=2)

print(f"\n共为 {patched_count} 个事件添加了种族变体")
